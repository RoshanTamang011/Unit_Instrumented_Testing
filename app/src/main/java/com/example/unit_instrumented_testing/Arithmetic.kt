package com.example.unit_instrumented_testing

class Arithmetic {

    fun add(f: Float, s: Float): Float {
        return f + s
    }

    fun subtract(f: Float, s: Float): Float {
        return f - s
    }

    fun multiply(f: Float, s: Float): Float {
        return f * s
    }
    fun divide(f: Float, s: Float): Float {
        return f / s
    }
}